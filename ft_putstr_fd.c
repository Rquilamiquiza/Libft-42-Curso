#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	int	i;
	int	len;

	i = 0;
	len = 0;
	while (s[i] != '\0')
		i++;
	len = (i * sizeof(char));
	write(fd, s, len);
}
