#include "libft.h"

char	*ft_strdup(const char *src)
{
	int		len;
	int		i;
	char	*copy;

	len = 0;
	i = 0;
	while (src[len])
		len++;
	copy = (char *)malloc((len + 1) * sizeof(char));
	if (copy == NULL)
		return (NULL);
	while (i < len)
	{
		copy[i] = src[i];
		i++;
	}
	copy[i] = '\0';
	return (copy);
}
