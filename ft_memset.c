#include "libft.h"

void	*ft_memset(void *ptr, int c, size_t n)
{
	size_t	i;
	char	*pt;

	i = 0;
	pt = ptr;
	while (i < n)
	{
		pt[i] = c;
		i++;
	}
	return (ptr);
}
